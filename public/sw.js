/**
 * CRIS Golf Program — Service Worker (subphase 1F; offline fix: full precache)
 *
 * Strategy: "App Shell + Static Precache"
 *   - On install: precache the complete app shell (all HTML pages, every
 *     _next/static JS/CSS chunk, woff2 fonts, icons, splash images, and
 *     manifest) so the app opens offline after the first visit.
 *   - On fetch: cache-first for precached assets; network-first for navigation
 *     requests (so a live deploy always wins over stale HTML once online).
 *
 * Precache manifest (CACHE_VERSION + URL list):
 *   Loaded via importScripts below.  The file is AUTO-GENERATED at build time
 *   by scripts/generate-sw-precache.mjs, which runs as a postbuild step after
 *   `next build`.  It walks out/ and emits EVERY asset the exported pages
 *   reference — including the hashed _next/static chunks that the hand-written
 *   list previously missed.  CACHE_VERSION is the Next.js BUILD_ID, so it
 *   changes automatically on every deploy whose output changes.
 *
 *   Source of truth: scripts/generate-sw-precache.mjs
 *   Output file:     out/precache-manifest.js  (deployed alongside sw.js)
 *   Do NOT edit the precache list here — edit the generator instead.
 *
 * Privacy (§8): This service worker makes NO third-party network calls.
 *   It does NOT use push notifications, background sync, or analytics.
 *   It only caches first-party assets from this same origin.
 *   youtube-nocookie embeds are NOT precached — they load from the network
 *   only when the user presses play (as in 1E), keeping the "no third-party
 *   contact on page load" guarantee intact.
 *
 * Update / versioning:
 *   CACHE_VERSION is now the Next.js BUILD_ID (injected via precache-manifest.js).
 *   No manual bumps needed — every `next build` generates a new BUILD_ID,
 *   which flows through to a new CACHE_NAME, which triggers old-cache cleanup
 *   in the activate event.  skipWaiting + clients.claim mean the new SW takes
 *   effect immediately for all open tabs.
 *
 * Testing note (iOS standalone):
 *   A service worker does not control the page on its very first visit.  For
 *   offline testing, launch the installed app once online, let it fully load,
 *   then turn on Airplane Mode and reload.  See progress.md for the full
 *   test sequence.
 */

// ─── Precache manifest ───────────────────────────────────────────────────────
// Loads self.__PRECACHE_VERSION and self.__PRECACHE_MANIFEST from the file
// generated by scripts/generate-sw-precache.mjs (postbuild step).
// The manifest lives at /precache-manifest.js and is deployed alongside sw.js.
importScripts("/precache-manifest.js");

// ─── Cache versioning ────────────────────────────────────────────────────────
// Derived from the Next.js BUILD_ID injected by precache-manifest.js.
// Falls back to a timestamp string if the manifest somehow failed to load
// (should never happen in a correct build, but keeps the SW from crashing).
const CACHE_VERSION = self.__PRECACHE_VERSION || `fallback-${Date.now()}`;
const CACHE_NAME = `cris-golf-${CACHE_VERSION}`;

// ─── URL list ────────────────────────────────────────────────────────────────
// Set by precache-manifest.js.  Includes all _next/static chunks + HTML routes
// + icons/splash + manifest — the complete app shell.
const PRECACHE_URLS = self.__PRECACHE_MANIFEST || [];

// ─── Install: precache all shell assets ──────────────────────────────────────
self.addEventListener("install", (event) => {
  event.waitUntil(
    caches
      .open(CACHE_NAME)
      .then((cache) => {
        // Cache each URL INDEPENDENTLY (not cache.addAll, which is atomic and
        // would discard the entire batch if any single URL 404s). This keeps
        // the precache resilient: one missing route never empties the cache.
        return Promise.allSettled(
          PRECACHE_URLS.map((u) =>
            cache.add(u).catch((err) => {
              console.warn("[SW] Precache failed for", u, err);
            })
          )
        );
      })
      .then(() => {
        // Skip the waiting phase so the new SW activates immediately.
        return self.skipWaiting();
      })
  );
});

// ─── Activate: delete stale caches ───────────────────────────────────────────
self.addEventListener("activate", (event) => {
  event.waitUntil(
    caches
      .keys()
      .then((keys) =>
        Promise.all(
          keys
            .filter((key) => key.startsWith("cris-golf-") && key !== CACHE_NAME)
            .map((key) => {
              console.log("[SW] Deleting stale cache:", key);
              return caches.delete(key);
            })
        )
      )
      .then(() => {
        // Claim all open clients immediately so the new cache is used at once.
        return self.clients.claim();
      })
  );
});

// ─── Fetch: serve from cache, fall back to network ───────────────────────────
self.addEventListener("fetch", (event) => {
  const { request } = event;
  const url = new URL(request.url);

  // Only handle same-origin requests — never intercept third-party calls
  // (youtube-nocookie, fonts, etc.) to respect §8.
  if (url.origin !== self.location.origin) {
    return; // let the browser handle it normally
  }

  // Navigation requests (HTML pages): network-first so a fresh deploy is
  // served immediately when online; fall back to cache when offline.
  if (request.mode === "navigate") {
    event.respondWith(
      fetch(request)
        .then((networkResponse) => {
          // Cache the fresh page for offline use
          if (networkResponse.ok) {
            caches.open(CACHE_NAME).then((cache) => {
              cache.put(request, networkResponse.clone());
            });
          }
          return networkResponse;
        })
        .catch(async () => {
          // Offline: serve the exact cached page if we have it.
          const cached = await caches.match(request);
          if (cached) return cached;

          // Otherwise fall back to the requested locale's root so an offline
          // user keeps their own language (e.g. /th for an uncached /th/gallery),
          // then English, then the bare root redirect. Each match is awaited
          // individually because caches.match() returns a Promise (which is
          // always truthy) — a plain `||` chain would never reach later options.
          const localeRoot = "/" + (url.pathname.split("/")[1] || "en");
          return (
            (await caches.match(localeRoot)) ||
            (await caches.match("/en")) ||
            (await caches.match("/"))
          );
        })
    );
    return;
  }

  // Static assets (JS, CSS, images, fonts): cache-first for speed.
  event.respondWith(
    caches.match(request).then((cached) => {
      if (cached) return cached;

      // Not in cache — fetch from network and cache for next time.
      return fetch(request).then((networkResponse) => {
        if (networkResponse.ok) {
          caches.open(CACHE_NAME).then((cache) => {
            cache.put(request, networkResponse.clone());
          });
        }
        return networkResponse;
      });
    })
  );
});
